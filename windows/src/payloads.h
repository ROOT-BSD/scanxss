/* Static XOR key — fixed at compile time, no runtime address dependence */
static inline char *px_dec(const unsigned char *enc, size_t len) {
    static const unsigned char KEY[64] = {
        0x09, 0xda, 0xf0, 0xda, 0xc1, 0xee, 0xa9, 0xca, 0xba, 0xd0, 0xc3, 0x89, 0x8d, 0x80, 0x80, 0x5a, 0xa1, 0xc7, 0x45, 0xf9, 0xd2, 0xa2, 0xf2, 0x43, 0x03, 0x4f, 0xc8, 0x5a, 0x98, 0x52, 0xb7, 0xfd, 0xaa, 0xe7, 0xc6, 0xe2, 0x84, 0x70, 0x2b, 0xeb, 0xaa, 0xff, 0x62, 0x05, 0x19, 0x4e, 0x8e, 0xf4, 0xbe, 0xef, 0x16, 0x45, 0xe2, 0x7a, 0x46, 0x9d, 0x40, 0xe0, 0xc3, 0x52, 0x39, 0xc0, 0x12, 0xa8
    };
    char *out = malloc(len + 1);
    if (!out) return NULL;
    for (size_t i = 0; i < len; i++)
        out[i] = (char)(enc[i] ^ KEY[i % 64]);
    out[len] = 0;
    return out;
}

