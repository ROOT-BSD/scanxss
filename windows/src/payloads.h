/* Runtime XOR decryption */
static inline char *px_dec(const unsigned char *enc, size_t len) {
    /* Key is built from process start time + module handle — not stored */
    static unsigned char key[64];
    static int key_init = 0;
    if (!key_init) {
        key_init = 1;
        /* Derive key from compile-time seed XORed with module base */
        unsigned char seed[64] = {
            0xf2, 0x8e, 0x20, 0xb7, 0xdf, 0x37, 0x47, 0xdd, 0xbc, 0x7e, 0xd8, 0x34, 0x3c, 0xb4, 0x46, 0x82, 0x06, 0x3c, 0x55, 0xbf, 0xdb, 0xff, 0xc5, 0xbb, 0x24, 0x65, 0x3c, 0x1b, 0xd5, 0xa1, 0xbe, 0xf0, 0xf4, 0x8c, 0xe0, 0xa4, 0x2f, 0xb5, 0xec, 0xc0, 0xe0, 0x99, 0x14, 0x0b, 0x2b, 0x5a, 0x9d, 0x46, 0xed, 0x95, 0x60, 0xd4, 0x89, 0xe9, 0x6c, 0x0f, 0xc6, 0x11, 0x99, 0xc5, 0x9f, 0x75, 0x09, 0xab
        };
        HMODULE hm = GetModuleHandleW(NULL);
        uintptr_t base = (uintptr_t)hm;
        for (int i = 0; i < 64; i++)
            key[i] = seed[i] ^ ((base >> (i % 8)) & 0xFF);
    }
    char *out = malloc(len + 1);
    for (size_t i = 0; i < len; i++)
        out[i] = enc[i] ^ key[i % 64];
    out[len] = 0;
    return out; /* caller must free() */
}

